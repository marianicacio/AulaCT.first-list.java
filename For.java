
public class For {
    //Printar 11 primeiros números

    public static void main(String[] args) {
        //Declaração do contador
        int i;
        //Iniciando o looping com valores de 0 atá 10
        for (i = 0; i < 100; i++) {
            //printar o contador
            System.out.println(i);
        }

    }

}
